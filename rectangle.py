def area(a, b):
    '''Принимает числа a и b, возвращает произведение чисел a и b'''
    if a > 0 and b > 0:
        return a * b
    return 0

def perimeter(a, b): 
    '''Принимает числа a и b, возвращает удвоенную сумму чисел a и b'''
    if a > 0 and b > 0:
        return 2 * (a + b)
    return 0
