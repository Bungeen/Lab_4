def area(a, h): 
    '''Принимает числа a и h, возвращает половину произведения a и h'''
    if a > 0 and h > 0:
        return a * h / 2
    return 0

def perimeter(a, b, c): 
    '''Принимает числа a, b, c, возвращает сумму a + b + c'''
    if a > 0 and b > 0 and c > 0:
        return a + b + c
    return 0
