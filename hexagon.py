def area(a):
    '''Принимает число a, возвращает произведение квадрата числа a на коэффициент 3 * sqrt(3) / 2'''
    return 3 ** 1.5 / 2 * a ** 2

def perimeter(a): 
    '''Принимает число a, возвращает 6 * a'''
    return 6 * a

def mainDiagonal(a):
    '''Принимает число a, возвращает удвоенное a'''
    return 2 * a

def subDiagonal(a):
    '''Принимает число a, возвращает sqrt(3) * a'''
    return 3 * a
